public class spaceH {
    
        public static void main(Strings args[])
        {
            int n = 4;
    
            //space
            for(int i=1;i<=n;i++)
            {
                for(int j=1;j<=n-i;j++)
                {
                    System.out.print(" ");
                }
                for(int j=1;j<=i;j++)
                {
                System.out.print("*");
                }
                System.out.println();
    
            }
        
        }
    
    
        
    
    
    



    


    
}
