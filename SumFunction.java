import java.util.*;
public class SumFunction {
 
    public static int  CalculateSum(int a, int b)
    {
        return a+b;
    
    }
    
    public static void main(Strings args[])
    {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = CalculateSum(a,b);
        System.out.println(sum);
    }
    
}


    

